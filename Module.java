public class Module {

    private int module1;
    private int module2;
    private int module3;

    public Module( int module1 , int module2 , int module3){
        this.module1 = module1;
        this.module2 = module2;
        this.module3 = module3;
    }

    public int getmodule1(){
        return this.module1;
    }
    public void setmodule1( int module1){
        this.module1 = module1;
    }

    public int getmodule2(){
        return this.module2;
    }
    public void setmodule2( int module1){
        this.module2 = module2;
    }

    public int getmodule3(){
        return this.module3;
    }
    public void setmodule3( int module1){
        this.module3 = module3;
    }
}
